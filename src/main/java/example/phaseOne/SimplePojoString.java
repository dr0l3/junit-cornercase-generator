package example.phaseOne;

public class SimplePojoString {
    private Integer a;
    private Byte b;
    private Long c;
    private Double d;
    private Short e;
    private Float f;
    private String g;

    public Integer getA() {
        return a;
    }

    public Byte getB() {
        return b;
    }

    public Long getC() {
        return c;
    }

    public Double getD() {
        return d;
    }

    public Short getE() {
        return e;
    }

    public Float getF() {
        return f;
    }

    public String getG() {
        return g;
    }
}
