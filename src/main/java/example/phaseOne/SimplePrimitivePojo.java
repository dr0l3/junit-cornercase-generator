package example.phaseOne;

public class SimplePrimitivePojo {
    private int a;
    private byte b;
    private long c;
    private double d;
    private short e;
    private float f;

    public int getA() {
        return a;
    }

    public byte getB() {
        return b;
    }

    public long getC() {
        return c;
    }

    public double getD() {
        return d;
    }

    public short getE() {
        return e;
    }

    public float getF() {
        return f;
    }
}
